import util = require('gulp-util');
import {ENV, CLIENT_NODE_MODULES_SRC, CLIENT_NODE_MODULES_DEST} from './config';

export = function client_node_modules(gulp, plugins) {
    return function () {
        if(ENV !== 'dev') return util.noop();

        return gulp
            .src(CLIENT_NODE_MODULES_SRC, {read: false})
            .pipe(gulp.dest(CLIENT_NODE_MODULES_DEST));
    };
};
